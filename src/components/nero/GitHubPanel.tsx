import { useState } from "react";
import { useAuth } from "@/contexts/AuthContext";
import { Github, Loader2, CheckCircle } from "lucide-react";
import { toast } from "sonner";
import { useProject } from "@/contexts/ProjectContext";

export function GitHubPanel() {
  const { user, githubToken } = useAuth();
  const { project } = useProject();
  const [repoName, setRepoName] = useState("");
  const [pushing, setPushing] = useState(false);
  const [pushed, setPushed] = useState(false);

  const pushToGitHub = async () => {
    if (!githubToken || !project || !repoName.trim()) {
      toast.error("Missing token, project, or repo name");
      return;
    }

    setPushing(true);
    try {
      // Create repo
      const createRes = await fetch("https://api.github.com/user/repos", {
        method: "POST",
        headers: {
          Authorization: `token ${githubToken}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          name: repoName,
          description: `Generated by Nero AI`,
          private: false,
          auto_init: true,
        }),
      });

      if (!createRes.ok && createRes.status !== 422) {
        throw new Error("Failed to create repo");
      }

      const repoData = createRes.status === 422
        ? { full_name: `${user?.user_metadata?.user_name}/${repoName}` }
        : await createRes.json();

      // Push files via GitHub API
      for (const file of project.files) {
        await fetch(
          `https://api.github.com/repos/${repoData.full_name}/contents/${file.path}`,
          {
            method: "PUT",
            headers: {
              Authorization: `token ${githubToken}`,
              "Content-Type": "application/json",
            },
            body: JSON.stringify({
              message: `Add ${file.path} via Nero AI`,
              content: btoa(unescape(encodeURIComponent(file.content))),
            }),
          }
        );
      }

      setPushed(true);
      toast.success(`Pushed to github.com/${repoData.full_name}`);
    } catch (err) {
      console.error(err);
      toast.error("Failed to push to GitHub");
    } finally {
      setPushing(false);
    }
  };

  if (!project || project.files.length === 0) {
    return (
      <div className="flex flex-col items-center justify-center h-full text-center px-6 gap-3">
        <Github className="w-10 h-10 text-muted-foreground" />
        <p className="text-sm text-muted-foreground">
          Generate some code first, then push it to GitHub.
        </p>
      </div>
    );
  }

  return (
    <div className="flex flex-col h-full bg-background p-4 gap-4">
      <div className="flex items-center gap-2">
        <Github className="w-5 h-5 text-foreground" />
        <h2 className="text-sm font-medium text-foreground">Push to GitHub</h2>
      </div>

      <p className="text-xs text-muted-foreground">
        Create a new repo or push to an existing one. {project.files.length} file(s) ready.
      </p>

      <input
        value={repoName}
        onChange={(e) => setRepoName(e.target.value)}
        placeholder="Repository name"
        className="w-full bg-nero-surface border border-border rounded-lg px-3 py-2.5 text-sm text-foreground placeholder:text-muted-foreground outline-none focus:ring-1 focus:ring-ring"
      />

      <button
        onClick={pushToGitHub}
        disabled={pushing || !repoName.trim()}
        className="w-full flex items-center justify-center gap-2 h-11 rounded-lg bg-foreground text-background font-medium text-sm hover:opacity-90 transition-opacity disabled:opacity-40"
      >
        {pushing ? (
          <Loader2 className="w-4 h-4 animate-spin" />
        ) : pushed ? (
          <CheckCircle className="w-4 h-4" />
        ) : (
          <Github className="w-4 h-4" />
        )}
        {pushing ? "Pushing..." : pushed ? "Pushed!" : "Push to GitHub"}
      </button>

      {pushed && (
        <a
          href={`https://github.com/${user?.user_metadata?.user_name}/${repoName}`}
          target="_blank"
          rel="noopener noreferrer"
          className="text-xs text-center text-muted-foreground underline hover:text-foreground transition-colors"
        >
          View on GitHub →
        </a>
      )}
    </div>
  );
}
